package ch.test;

public final class Calculator {

    public Calculator() {
    }

    public int add(final int a, final int b) {
        return a + b;
    }

    public int substract(final int a, final int b) {
        return a - b;
    }

    public int square(final int a) {
        return a * a;
    }

    public int multiply(final int a, final int b) {
        return a * b;
    }
}
