package ch.test;

public class Calculator {

    public int add(int a, int b) {
        return a + b;
    }

    public int substract(int a, int b) {
        return a - b;
    }

    public int square(int a) {
        return a * a;
    }

    public int multiply(int a, int b) {
        return a * b;
    }
}
